// @route   POST /api/questions/generate
// @desc    AI generates NEET questions (MCQ, Assertion-Reason, Match, Statement)
//          including PYQ-style and predicted questions, with full classification.
// @access  Private/Admin
// ─────────────────────────────────────────────────────────────────────────────
exports.generateQuestions = async (req, res) => {
  try {
    const aiService = require('../services/aiQuestionGenerator');
    const {
      subject = 'biology',
      chapter = '',
      topic = '',
      questionTypes = ['mcq'],
      count = 10,
      includePYQ = true,
      includePredicted = true,
      difficulty = 'mixed',
      source = 'custom'
    } = req.body || {};

    const candidates = await aiService.generateQuestions({
      subject, chapter, topic,
      questionTypes, count, includePYQ, includePredicted, difficulty, source
    });

    if (!Array.isArray(candidates) || candidates.length === 0) {
      return res.status(502).json({ success: false, message: 'AI returned no questions' });
    }

    // Attach uploader and save as drafts
    const toSave = candidates.map(q => ({ ...q, uploadedBy: req.userId }));
    const saved = await Question.insertMany(toSave);

    res.status(201).json({
      success: true,
      message: `${saved.length} questions generated by AI and saved as drafts. Review before publishing.`,
      data: saved
    });
  } catch (error) {
    console.error('❌ AI Generate Error:', error);
    res.status(500).json({ success: false, message: error.message || 'AI generation failed' });
  }
};

// Stream generation: call the AI repeatedly for single-question outputs and stream each saved draft via SSE
exports.generateQuestionsStream = async (req, res) => {
  try {
    const aiService = require('../services/aiQuestionGenerator');
    const {
      subject = 'biology',
      chapter = '',
      topic = '',
      questionTypes = ['mcq'],
      count = 10,
      includePYQ = true,
      includePredicted = true,
      difficulty = 'mixed',
      source = 'custom'
    } = req.body || {};

    const total = Math.max(1, Math.min(50, Number(count) || 10));

    // SSE headers
    res.writeHead(200, {
      'Content-Type': 'text/event-stream',
      'Cache-Control': 'no-cache',
      Connection: 'keep-alive'
    });
    res.flushHeaders?.();

    let idx = 0;
    for (idx = 1; idx <= total; idx++) {
      // If client closed connection, stop
      if (res.finished) break;

      // Request one question at a time from AI
      let candidates;
      try {
        candidates = await aiService.generateCandidates({ subject, chapter, topic, difficulty, count: 1 });
      } catch (err) {
        const payload = { type: 'error', index: idx, total, message: err.message };
        res.write(`event: error\ndata: ${JSON.stringify(payload)}\n\n`);
        break;
      }

      const q = Array.isArray(candidates) && candidates[0] ? candidates[0] : null;
      if (!q) {
        const payload = { type: 'skipped', index: idx, total, message: 'AI returned no question' };
        res.write(`event: skipped\ndata: ${JSON.stringify(payload)}\n\n`);
        continue;
      }

      // Normalize to DB shape similar to generateQuestions
      const OPTION_KEYS = ['A', 'B', 'C', 'D'];
      const options = {};
      OPTION_KEYS.forEach((k, i) => {
        const keyText = q.options?.[k]?.text || (q.options && q.options[i] && q.options[i].text) || `Option ${k}`;
        options[k] = { text: String(keyText).trim() };
      });

      const savedObj = {
        questionText: q.questionText || `AI generated question ${idx}`,
        options,
        correctAnswer: q.correctAnswer && ['A','B','C','D'].includes(q.correctAnswer) ? q.correctAnswer : 'A',
        explanation: { text: q.explanation || '' },
        subject: (subject || 'biology').toLowerCase(),
        chapter: q.chapter || chapter || 'Unclassified',
        topic: q.topic || topic || '',
        type: q.type || 'mcq',
        difficulty: q.difficulty || difficulty === 'mixed' ? 'medium' : difficulty,
        source: q.source || (q.isLikelyPYQ ? 'pyq' : source),
        sourceDetails: { year: q.yearSeen || undefined, examType: 'neet' },
        pyq: { isPYQ: Boolean(q.isLikelyPYQ), reference: q.isLikelyPYQ ? `NEET ${q.yearSeen || 'PYQ'}` : undefined },
        assertion: q.assertion || null,
        generatedByAI: true,
        aiMetadata: { generatedAt: new Date(), confidence: q.confidenceScore },
        trendingFrequency: q.difficulty === 'hard' ? 'high' : q.difficulty === 'easy' ? 'low' : 'medium',
        inSyllabus: true,
        tags: ['ai-generated', q.type || 'mcq', q.difficulty || 'medium'],
        qualityScore: q.qualityScore || 70,
        isPublished: false,
        review: { status: 'pending' },
        uploadedBy: req.userId
      };

      try {
        const saved = await Question.create(savedObj);
        const payload = { type: 'question', index: idx, total, data: saved };
        res.write(`event: question\ndata: ${JSON.stringify(payload)}\n\n`);
      } catch (saveErr) {
        const payload = { type: 'error', index: idx, total, message: saveErr.message };
        res.write(`event: error\ndata: ${JSON.stringify(payload)}\n\n`);
      }
    }

    // Final event
    res.write(`event: done\ndata: ${JSON.stringify({ total: idx - 1 })}\n\n`);
    res.end();
  } catch (error) {
    console.error('❌ Stream Generate Error:', error);
    if (!res.headersSent) res.status(500).json({ success: false, message: error.message });
    else {
      try {
        res.write(`event: error\ndata: ${JSON.stringify({ message: error.message })}\n\n`);
        res.end();
      } catch (e) {}
    }
  }
};
// @route   POST /api/questions/:id/ai-fix
// @desc    Use AI to clean up a garbled question AND classify difficulty/topic/chapter
// @access  Private/Admin
exports.aiFixQuestion = async (req, res) => {
  try {
    const question = await Question.findById(req.params.id);
    if (!question) {
      return res.status(404).json({ success: false, message: 'Question not found' });
    }

    const { PHYSICS_CHAPTERS, CHEMISTRY_CHAPTERS, BIOLOGY_CHAPTERS } = require('../config/constants');
    const chapterMap = {
      physics: PHYSICS_CHAPTERS, chemistry: CHEMISTRY_CHAPTERS,
      biology: BIOLOGY_CHAPTERS, botany: BIOLOGY_CHAPTERS, zoology: BIOLOGY_CHAPTERS
    };
    const chapterList = (chapterMap[(question.subject || '').toLowerCase()] || [
      ...PHYSICS_CHAPTERS, ...CHEMISTRY_CHAPTERS, ...BIOLOGY_CHAPTERS
    ]).join(', ');

    const optionsText = ['A', 'B', 'C', 'D']
      .map(k => `${k}: ${question.options?.[k]?.text || ''}`)
      .join('\n');

    const prompt = `You are a NEET question editor and classifier. The question below may have garbled text from PDF extraction. Do two things in one pass:

1. CLEAN the question — fix garbled text, truncated content, bleeding answer explanations.
2. CLASSIFY the question — assign difficulty, topic, and the best NEET NCERT chapter.

Subject: ${question.subject}
Current question stem: ${question.questionText}
Current options:
${optionsText}
Current correct answer: ${question.correctAnswer}

NEET Chapter list: ${chapterList}

Rules:
1. Return ONLY valid JSON, no markdown.
2. "questionText" — clean, complete question sentence only.
3. Each option A/B/C/D — only the option text, no trailing explanation.
4. "correctAnswer" — A, B, C, or D. Deduce from context if missing.
5. "explanation" — concise 1-2 sentence explanation of the correct answer.
6. "difficulty" — "easy", "medium", or "hard" based on NEET standards.
7. "topic" — specific sub-topic or concept being tested (e.g. "Food chain", "Ohm's Law").
8. "chapter" — best matching NCERT chapter from the list above.
9. If you cannot fix this question at all, return {"error": "cannot fix"}.

Return exactly:
{"questionText":"...","options":{"A":{"text":"..."},"B":{"text":"..."},"C":{"text":"..."},"D":{"text":"..."}},"correctAnswer":"A","explanation":"...","difficulty":"medium","topic":"...","chapter":"..."}`;

    let fixed;
    try {
      const content = await getGeminiText({
        prompt,
        maxOutputTokens: 1000,
        temperature: 0.1,
        responseMimeType: 'application/json'
      });

      const jsonMatch = content.match(/\{[\s\S]*\}/);
      if (!jsonMatch) throw new Error('No JSON in response');
      fixed = JSON.parse(jsonMatch[0]);
      if (fixed.error) throw new Error(fixed.error);
    } catch (aiError) {
      return res.status(422).json({
        success: false,
        message: `AI could not fix this question: ${aiError.message}`
      });
    }

    const OPTION_KEYS = ['A', 'B', 'C', 'D'];
    const hasAllOptions = OPTION_KEYS.every(k => fixed.options?.[k]?.text?.trim().length > 0);
    const hasValidAnswer = OPTION_KEYS.includes(fixed.correctAnswer);
    if (!fixed.questionText?.trim() || !hasAllOptions || !hasValidAnswer) {
      return res.status(422).json({
        success: false,
        message: 'AI returned incomplete question data. Please fix manually.'
      });
    }

    const validDifficulty = ['easy', 'medium', 'hard'].includes(fixed.difficulty) ? fixed.difficulty : (question.difficulty || 'medium');

    const updated = await Question.findByIdAndUpdate(
      req.params.id,
      {
        questionText: fixed.questionText.trim(),
        options: Object.fromEntries(
          OPTION_KEYS.map(k => [k, { text: fixed.options[k].text.trim(), image: question.options?.[k]?.image }])
        ),
        correctAnswer: fixed.correctAnswer,
        explanation: fixed.explanation ? { text: fixed.explanation } : question.explanation,
        difficulty: validDifficulty,
        topic: fixed.topic?.trim() || question.topic || '',
        chapter: fixed.chapter?.trim() || question.chapter || 'Unclassified',
        tags: [...new Set([...(question.tags || []).filter(t => t !== 'raw-import'), 'ai-fixed'])]
      },
      { new: true, runValidators: true }
    );

    res.status(200).json({
      success: true,
      message: 'Question fixed and classified by AI.',
      data: updated
    });

  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};


// @route   POST /api/questions/upload-pdf
// @desc    Upload PDF for question extraction (Admin only)
// @access  Private/Admin
exports.uploadPDF = async (req, res) => {
  try {
    if (!req.file) {
      return res.status(400).json({
        success: false,
        message: 'No file uploaded'
      });
    }

    const { subject, chapter, topic, source, testType, year, allowLocalOcr, useAiParsing } = req.body;

    const { questions, images } = await PDFExtractor.parseQuestionsFromFile(req.file, {
      subject,
      chapter,
      topic,
      source,
      sourceDetails: {
        year: year ? Number(year) : undefined,
        testName: req.file.originalname,
        examType: testType
      }
    }, {
      allowLocalOcr: allowLocalOcr === 'true',
      useAiParsing: useAiParsing === 'true'
    });

    // Save questions to database
    const savedQuestions = await Question.insertMany(
      questions.map(q => ({
        ...q,
        uploadedBy: req.userId,
        isPublished: false
      }))
    );

    // ── Auto-classify: let AI assign difficulty, topic, chapter right away ──
    // Run silently in the background — failures don't block the response.
    const autoClassify = async () => {
      const batchSize = 20;
      for (let i = 0; i < savedQuestions.length; i += batchSize) {
        const batch = savedQuestions.slice(i, i + batchSize);
        try {
          const classifications = await PDFExtractor.classifyQuestionBatch(batch);
          const byId = new Map(classifications.map(item => [String(item.id), item]));
          for (const q of batch) {
            const c = byId.get(String(q._id));
            if (!c) continue;
            const tags = Array.from(new Set([
              ...(q.tags || []).filter(t => t !== 'raw-import'),
              ...(c.tags || []),
              'ai-classified'
            ])).filter(Boolean);
            await Question.findByIdAndUpdate(q._id, {
              difficulty: c.difficulty,
              topic: c.topic,
              chapter: c.chapter || q.chapter,
              learningObjective: c.learningObjective,
              commonMistake: c.commonMistake,
              tags
            });
          }
        } catch (classifyErr) {
          console.warn(`[Auto-classify] Batch ${Math.floor(i / batchSize) + 1} failed:`, classifyErr.message);
        }
      }
    };

    // Fire-and-forget — don't await so the HTTP response returns immediately
    autoClassify().catch(err => console.warn('[Auto-classify] Error:', err.message));

    res.status(201).json({
      success: true,
      message: `${savedQuestions.length} questions extracted and saved. AI is classifying difficulty & topics in the background.`,
      data: savedQuestions,
      extractedImages: images || []
    });
  } catch (error) {
    console.error('❌ PDF Upload Error:', error);
    const message = error.message || 'PDF processing failed';
    const { getProvider } = require('../services/geminiClient');
    const provider = getProvider();

    const isQuotaError = error.status === 429 ||
      /throttl|quota|rate limit|too many requests|retry after|insufficient/i.test(message);

    const isAccessError = /access denied|not authorized|AccessDenied|permission|InvokeModel|authentication|api.key/i.test(message);

    const isExpectedImportError = isQuotaError || isAccessError ||
      /API_KEY|AI question extraction|API key|model|scanned|OCR|extract text|parse questions|No valid JSON|PDF|unsupported|empty|OpenAI|Bedrock|Gemini/i.test(message);

    let action = message;
    if (isQuotaError) {
      if (provider === 'openai') {
        action = 'OpenAI rate limit or quota exceeded. Please check billing/limits at https://platform.openai.com/account/billing';
      } else if (provider === 'bedrock') {
        action = 'AWS Bedrock throttling limit reached. Please request a model invocation quota increase in the AWS Bedrock Console.';
      } else {
        action = 'Gemini rate limit exceeded. Wait a moment and retry, or request quota in Google AI Studio.';
      }
    } else if (isAccessError) {
      if (provider === 'openai') {
        action = 'OpenAI authentication failed. Set a valid OPENAI_API_KEY in backend/.env starting with "sk-".';
      } else if (provider === 'bedrock') {
        action = 'AWS Bedrock access denied. Check bedrock:InvokeModel permission and enable Claude 3 Haiku in Model Access (us-east-1).';
      } else {
        action = 'Gemini authentication failed. Set a valid GEMINI_API_KEY in backend/.env.';
      }
    } else if (/poppler|pdftoppm|renderer|local OCR/i.test(message)) {
      action = 'Install Poppler so local OCR can render scanned PDF pages, set PDF_RENDERER_PATH in backend/.env.';
    } else if (message.toLowerCase().includes('scanned')) {
      action = 'This PDF is scanned/image-based. Use a selectable-text PDF, or add questions manually.';
    }

    res.status(isQuotaError ? 429 : isExpectedImportError ? 400 : 500).json({
      success: false,
      message,
      action,
      retryAfter: error.retryAfter
    });
  } finally {
    if (req.file?.path) {
      const fs = require('fs');
      fs.unlink(req.file.path, () => {});
    }
  }
};

