exports.getAdminQuestions = async (req, res) => {
  try {
    const { status = 'pending', page = 1, limit = 50, search, subject, chapter } = req.query;
    
    let query = {};
    if (status === 'pending') {
      query.isPublished = false;
    } else if (status === 'published') {
      query.isPublished = true;
    }

    if (subject) {
      query.subject = subject.toLowerCase();
    }
    if (chapter) {
      query.chapter = chapter;
    }

    if (search && String(search).trim()) {
      const pattern = new RegExp(String(search).trim().replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'i');
      query.$or = [
        { questionText: pattern },
        { chapter: pattern },
        { topic: pattern }
      ];
    }

    const skip = (Number(page) - 1) * Number(limit);
    const [questions, total] = await Promise.all([
      Question.find(query).sort({ createdAt: -1 }).skip(skip).limit(Number(limit)),
      Question.countDocuments(query)
    ]);
    res.status(200).json({
      success: true,
      data: questions,
      pagination: { total, page: Number(page), pages: Math.ceil(total / Number(limit)) }
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

// ─────────────────────────────────────────────────────────────────────────────
// @route   POST /api/questions/generate
// @desc    AI generates NEET questions (MCQ, Assertion-Reason, Match, Statement)
//          including PYQ-style and predicted questions, with full classification.
// @access  Private/Admin
// ─────────────────────────────────────────────────────────────────────────────
exports.generateQuestions = async (req, res) => {
  try {
    const {
      subject = 'biology',
      chapter = '',
      topic = '',
      questionTypes = ['mcq'],       // ['mcq','assertion_reason','match_following','statement_based']
      count = 10,                    // per type
      includePYQ = true,             // include PYQ-style questions
      includePredicted = true,       // include predicted/likely questions
      difficulty = 'mixed',          // 'easy'|'medium'|'hard'|'mixed'
      source = 'custom'
    } = req.body;

    const { PHYSICS_CHAPTERS, CHEMISTRY_CHAPTERS, BIOLOGY_CHAPTERS } = require('../config/constants');
    const chapterMap = {
      physics: PHYSICS_CHAPTERS, chemistry: CHEMISTRY_CHAPTERS,
      biology: BIOLOGY_CHAPTERS, botany: BIOLOGY_CHAPTERS, zoology: BIOLOGY_CHAPTERS
    };
    const chapterList = (chapterMap[subject.toLowerCase()] || []).join(', ');

    const safeCount = Math.min(20, Math.max(1, Number(count) || 10));
    const safeTypes = Array.isArray(questionTypes) ? questionTypes : [questionTypes];

    const difficultyInstruction = difficulty === 'mixed'
      ? 'Distribute difficulty: roughly 30% easy, 40% medium, 30% hard.'
      : `All questions should be ${difficulty} difficulty.`;

    // Build a type-specific prompt section
    const typeInstructions = {
      mcq: `
MCQ FORMAT:
{
  "type": "mcq",
  "questionText": "Complete question stem",
  "options": {"A":{"text":"..."},"B":{"text":"..."},"C":{"text":"..."},"D":{"text":"..."}},
  "correctAnswer": "A",
  "explanation": "Why the answer is correct",
  "difficulty": "easy|medium|hard",
  "topic": "specific sub-topic",
  "isLikelyPYQ": true/false,
  "yearSeen": 2023
}`,
      assertion_reason: `
ASSERTION-REASON FORMAT:
Assertion (A): [statement]
Reason (R): [statement]
Options MUST always be:
A: Both A and R are true and R is the correct explanation of A
B: Both A and R are true but R is not the correct explanation of A
C: A is true but R is false
D: A is false but R is true
{
  "type": "assertion_reason",
  "questionText": "Assertion (A): ...\\nReason (R): ...",
  "options": {
    "A": {"text": "Both A and R are true and R is the correct explanation of A"},
    "B": {"text": "Both A and R are true but R is not the correct explanation of A"},
    "C": {"text": "A is true but R is false"},
    "D": {"text": "A is false but R is true"}
  },
  "correctAnswer": "A|B|C|D",
  "explanation": "...",
  "difficulty": "easy|medium|hard",
  "topic": "...",
  "isLikelyPYQ": true/false
}`,
      match_following: `
MATCH THE FOLLOWING FORMAT:
Column I has items 1-4, Column II has items a-d.
{
  "type": "match_following",
  "questionText": "Match Column I with Column II:\\nColumn I\\n1. [item]\\n2. [item]\\n3. [item]\\n4. [item]\\nColumn II\\na. [match]\\nb. [match]\\nc. [match]\\nd. [match]",
  "options": {
    "A": {"text": "1-a, 2-b, 3-c, 4-d"},
    "B": {"text": "1-b, 2-a, 3-d, 4-c"},
    "C": {"text": "1-c, 2-d, 3-a, 4-b"},
    "D": {"text": "1-d, 2-c, 3-b, 4-a"}
  },
  "correctAnswer": "A|B|C|D",
  "explanation": "...",
  "difficulty": "easy|medium|hard",
  "topic": "...",
  "isLikelyPYQ": false
}`,
      statement_based: `
STATEMENT-BASED FORMAT (multiple true/false statements):
{
  "type": "statement_based",
  "questionText": "Which of the following statements are correct?\\n1. [statement]\\n2. [statement]\\n3. [statement]\\n4. [statement]",
  "options": {
    "A": {"text": "1 and 2 only"},
    "B": {"text": "2 and 3 only"},
    "C": {"text": "1, 2 and 3 only"},
    "D": {"text": "All of the above"}
  },
  "correctAnswer": "A|B|C|D",
  "explanation": "...",
  "difficulty": "easy|medium|hard",
  "topic": "...",
  "isLikelyPYQ": false
}`
    };

    const requestedTypeInstructions = safeTypes
      .map(t => typeInstructions[t] || typeInstructions.mcq)
      .join('\n\n');

    const pyqInstruction = includePYQ
      ? `- Include some questions that are actual PYQ-style (previously asked in NEET 2017-2024). Set "isLikelyPYQ": true and "yearSeen" for those.`
      : '';

    const predictedInstruction = includePredicted
      ? `- Include some predicted questions that are highly likely to appear in upcoming NEET based on recent trends and NTA patterns.`
      : '';

    const prompt = `You are an expert NEET question setter with deep knowledge of NCERT syllabus and NEET exam patterns.

Generate ${safeCount} high-quality NEET questions for:
- Subject: ${subject.toUpperCase()}
- Chapter: ${chapter || 'Any chapter from ' + subject}
- Topic: ${topic || 'Any topic in the chapter'}
- Types requested: ${safeTypes.join(', ')}
- ${difficultyInstruction}
${pyqInstruction}
${predictedInstruction}

IMPORTANT RULES:
1. Questions must be 100% NCERT-based and NEET-relevant.
2. All options must be scientifically accurate.
3. Explanation must be detailed enough for a student to understand why.
4. "topic" must be the specific concept tested (e.g., "Krebs Cycle", "Ohm's Law", "Mole Concept").
5. "difficulty": easy = recall/direct NCERT, medium = application, hard = multi-concept/analysis.
6. Return ONLY a valid JSON array — no markdown, no extra text.
7. For MCQ: all 4 options must be plausible but only one correct.
8. Available NEET chapters for chapter classification: ${chapterList}

QUESTION FORMAT for each type:
${requestedTypeInstructions}

Return a JSON array of exactly ${safeCount} questions:
[{ ... }, { ... }, ...]`;

    const content = await getGeminiText({
      systemInstruction: 'You are an expert NEET exam question setter. Return only valid JSON arrays.',
      prompt,
      maxOutputTokens: 4096,
      temperature: 0.7,
      responseMimeType: 'application/json'
    });

    // Parse AI response
    const jsonMatch = content.match(/\[[\s\S]*\]/);
    if (!jsonMatch) throw new Error('AI did not return a valid JSON array.');
    const generated = JSON.parse(jsonMatch[0]);

    if (!Array.isArray(generated) || generated.length === 0) {
      throw new Error('AI returned an empty question list.');
    }

    const OPTION_KEYS = ['A', 'B', 'C', 'D'];
    const validDifficulties = ['easy', 'medium', 'hard'];
    const validTypes = ['mcq', 'assertion_reason', 'match_following', 'statement_based', 'numerical'];

    // Normalize and validate each generated question
    const normalized = generated.map((q, idx) => {
      const qType = validTypes.includes(q.type) ? q.type : 'mcq';
      const qDiff = validDifficulties.includes(q.difficulty) ? q.difficulty : 'medium';

      // Ensure all 4 options exist
      const options = {};
      OPTION_KEYS.forEach(k => {
        options[k] = { text: q.options?.[k]?.text?.trim() || `Option ${k}` };
      });

      const correctAnswer = OPTION_KEYS.includes(q.correctAnswer) ? q.correctAnswer : 'A';

      const assertion = {
        isAssertionReason: qType === 'assertion_reason',
        assertionText: '',
        reasonText: '',
        pairIsCorrect: typeof q.pairIsCorrect === 'boolean' ? q.pairIsCorrect : undefined
      };
      if (qType === 'assertion_reason' && typeof q.questionText === 'string') {
        const assertionMatch = q.questionText.match(/Assertion\s*[:\-]?\s*(.+?)\s*(?=Reason|R\s*[:\-]?)/is);
        const reasonMatch = q.questionText.match(/Reason\s*[:\-]?\s*(.+)$/is);
        assertion.assertionText = assertionMatch ? assertionMatch[1].trim() : '';
        assertion.reasonText = reasonMatch ? reasonMatch[1].trim() : '';
      }
      if (qType !== 'assertion_reason') {
        assertion.isAssertionReason = false;
      }

      const questionText = (q.questionText || `Question ${idx + 1}`).trim();
      const pyqMeta = {
        isPYQ: Boolean(q.isLikelyPYQ),
        reference: q.isLikelyPYQ ? `NEET ${q.yearSeen || 'Recent PYQ'}` : undefined
      };

      return {
        questionText,
        options,
        correctAnswer,
        explanation: { text: q.explanation?.trim() || '' },
        subject: subject.toLowerCase(),
        chapter: q.chapter?.trim() || chapter || 'Unclassified',
        topic: q.topic?.trim() || topic || '',
        type: qType,
        difficulty: qDiff,
        source: q.isLikelyPYQ ? 'pyq' : source,
        sourceDetails: {
          year: q.yearSeen || undefined,
          examType: 'neet'
        },
        pyq: pyqMeta,
        assertion,
        generatedByAI: true,
        aiMetadata: {
          generatedAt: new Date()
        },
        trendingFrequency: qDiff === 'hard' ? 'high' : qDiff === 'easy' ? 'low' : 'medium',
        inSyllabus: true,
        tags: [
          'ai-generated',
          qType,
          qDiff,
          ...(q.isLikelyPYQ ? ['pyq-style'] : []),
          ...(includePredicted && !q.isLikelyPYQ ? ['predicted'] : [])
        ],
        qualityScore: 70,
        isPublished: false,
        review: {
          status: 'pending'
        }
      };
    });

    // Save to DB as drafts
    const saved = await Question.insertMany(
      normalized.map(q => ({ ...q, uploadedBy: req.userId }))
    );

    res.status(201).json({
      success: true,
      message: `${saved.length} questions generated by AI and saved as drafts. Review before publishing.`,
      data: saved
    });

  } catch (error) {
    console.error('❌ AI Generate Error:', error);
    const msg = error.message || 'AI generation failed';
    res.status(500).json({ success: false, message: msg });
  }
};
