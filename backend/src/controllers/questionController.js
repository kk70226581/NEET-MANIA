// @route   POST /api/questions/classify
// @desc    Classify saved questions in Gemini batches (Admin only)
// @access  Private/Admin
exports.classifyQuestions = async (req, res) => {
  try {
    const {
      questionIds,
      ids,
      subject,
      source,
      onlyUnclassified = true,
      limit = 500,
      batchSize = 50,
      publish = false
    } = req.body || {};

    const selectedIds = Array.isArray(questionIds) ? questionIds : Array.isArray(ids) ? ids : [];
    const safeLimit = Math.min(500, Math.max(1, Number(limit) || 500));
    const safeBatchSize = Math.min(50, Math.max(1, Number(batchSize) || 50));
    const query = {};

    if (selectedIds.length) {
      query._id = { $in: selectedIds };
    } else {
      if (subject) query.subject = subject;
      if (source) query.source = source;
      if (onlyUnclassified !== false) {
        query.$or = [
          { chapter: { $in: ['', 'Unclassified'] } },
          { topic: { $in: ['', null] } },
          { tags: 'raw-import' }
        ];
      }
    }

    const questions = await Question.find(query)
      .sort({ createdAt: -1 })
      .limit(safeLimit)
      .select('_id questionText options subject chapter topic difficulty tags learningObjective commonMistake');

    if (!questions.length) {
      return res.status(200).json({
        success: true,
        message: 'No questions matched the classification filter',
        data: { matched: 0, updated: 0, batches: 0 }
      });
    }

    let updated = 0;
    const failures = [];

    for (let index = 0; index < questions.length; index += safeBatchSize) {
      const batch = questions.slice(index, index + safeBatchSize);
      const classifications = await PDFExtractor.classifyQuestionBatch(batch);
      const byId = new Map(classifications.map(item => [String(item.id), item]));

      for (const question of batch) {
        const classification = byId.get(String(question._id));
        if (!classification) {
          failures.push({ id: String(question._id), reason: 'No classification returned' });
          continue;
        }

        let tags = Array.from(new Set([
          ...(question.tags || []),
          ...classification.tags,
          'ai-classified'
        ])).filter(Boolean);

        const needsAnswerReview = tags.includes('needs-answer-review') || question.sourceDetails?.needsAnswerReview;

        const updateObj = {
          chapter: classification.chapter,
          topic: classification.topic,
          difficulty: classification.difficulty,
          learningObjective: classification.learningObjective,
          commonMistake: classification.commonMistake,
          inSyllabus: classification.inSyllabus !== false,
          trendingFrequency: classification.trendingFrequency || 'medium'
        };

        if (classification.correctAnswer && (needsAnswerReview || !question.explanation?.text)) {
          updateObj.correctAnswer = classification.correctAnswer;
          updateObj.explanation = { text: classification.explanation || '' };
          // Remove the review flags now that AI solved it
          tags = tags.filter(t => t !== 'needs-answer-review');
          if (updateObj.sourceDetails) {
            updateObj.sourceDetails.needsAnswerReview = false;
          }
        }

        updateObj.tags = tags;

        if (publish) {
          updateObj.isPublished = true;
          updateObj.isVerified = true;
          updateObj.verifiedAt = new Date();
          if (req.user?._id) {
            updateObj.verifiedBy = req.user._id;
          }
        }

        await Question.findByIdAndUpdate(question._id, updateObj, { runValidators: true });

        updated += 1;
      }
    }

    res.status(200).json({
      success: true,
      message: `${updated} questions classified${publish ? ' and published' : ''}`,
      data: {
        matched: questions.length,
        updated,
        batches: Math.ceil(questions.length / safeBatchSize),
        failures
      }
    });
  } catch (error) {
    const message = error.message || 'Question classification failed';
    const isQuotaError = error.status === 429 || /quota|rate limit|retry after|too many requests/i.test(message);

    res.status(isQuotaError ? 429 : 500).json({
      success: false,
      message,
      action: isQuotaError
        ? 'Gemini quota is exhausted. Retry later, lower batchSize/limit, or request quota in Google AI Studio.'
        : 'Check backend logs and Gemini configuration.',
      retryAfter: error.retryAfter
    });
  }
};

// @route   DELETE /api/questions/admin/clear-all
// @desc    Delete questions selectively or all (admin only)
// @access  Private/Admin
exports.clearAllQuestions = async (req, res) => {
  try {
    const mongoose = require('mongoose');
    const { subject, chapter, topic, status } = req.query;

    const query = {};
    if (subject) query.subject = subject.toLowerCase().trim();
    if (chapter) query.chapter = chapter.trim();
    if (topic) query.topic = topic.trim();

    if (status === 'draft') {
      query.isPublished = false;
    } else if (status === 'published') {
      query.isPublished = true;
    }

    const questionResult = await Question.deleteMany(query);
    let attemptCount = 0;

    // Only clear attempts/tests if doing a complete database wipe (no filters)
    const isFullClear = !subject && !chapter && !topic && (!status || status === 'all');
    if (isFullClear) {
      if (mongoose.connection.collections['testattempts']) {
        const r = await mongoose.connection.collections['testattempts'].deleteMany({});
        attemptCount = r.deletedCount || 0;
      }
      if (mongoose.connection.collections['tests']) {
        await mongoose.connection.collections['tests'].deleteMany({});
      }
    }

    res.status(200).json({
      success: true,
      message: `Cleared ${questionResult.deletedCount} questions.${isFullClear ? ` Also cleared ${attemptCount} test attempts.` : ''}`,
      data: { questionsDeleted: questionResult.deletedCount, attemptsDeleted: attemptCount }
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

// @route   GET /api/questions/stats
// @desc    Get question statistics
// @access  Public
exports.getQuestionStats = async (req, res) => {
  try {
    const bySubject = await Question.aggregate([
      { $match: { isPublished: true } },
      {
        $group: {
          _id: '$subject',
          count: { $sum: 1 }
        }
      }
    ]);

    res.status(200).json({
      success: true,
      data: {
        totalPublished: await Question.countDocuments({ isPublished: true }),
        totalPending: await Question.countDocuments({ isPublished: false }),
        bySubject
      }
    });

  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message
    });
  }
};

